#!/bin/bash

# codegen.sh runs inside the go-build Docker container and generates all Go
# artifacts: CRDs, defaults, deep copies, OpenAPI specs, and client sets.
# Called by generate.sh, which handles the Docker invocation and host-level
# post-processing (prettier, CRD patches).

set -eo pipefail

: "${PACKAGE_NAME:?PACKAGE_NAME must be set}"

REPO_ROOT="/go/src/${PACKAGE_NAME}"
HEADER="${REPO_ROOT}/hack/boilerplate/boilerplate.go.txt"

###############################################################################
# CRD generation
###############################################################################
controller-gen \
	crd:allowDangerousTypes=true,crdVersions=v1,deprecatedV1beta1CompatibilityPreserveUnknownFields=false,maxDescLen=0 \
	paths=./pkg/apis/... \
	output:crd:dir=config/crd/

# Remove the first yaml separator line.
find ./config/crd -name "*.yaml" | xargs sed -i 1d

# Remove the profiles CRD — profiles are backed by Namespaces and don't need a CRD.
rm -f config/crd/projectcalico.org_profiles.yaml

###############################################################################
# Go code generation
###############################################################################
defaulter-gen \
	--v 1 --logtostderr \
	--go-header-file "${HEADER}" \
	--extra-peer-dirs "${PACKAGE_NAME}/pkg/apis/projectcalico/v3" \
	--output-file zz_generated.defaults.go \
	"${PACKAGE_NAME}/pkg/apis/projectcalico/v3"

deepcopy-gen \
	--v 1 --logtostderr \
	--go-header-file "${HEADER}" \
	--bounding-dirs "${PACKAGE_NAME}" \
	--output-file zz_generated.deepcopy.go \
	"${PACKAGE_NAME}/pkg/apis/projectcalico/v3"

# Generate OpenAPI model name accessors for Calico types. These implement
# the OpenAPIModelNamer interface so the apiserver's OpenAPI spec uses
# REST-friendly definition names (dots) instead of raw Go package paths
# (slashes). The openapi definition output goes to /tmp since we only need
# the model name files; the real openapi definitions are generated by the
# next step with all input packages.
openapi-gen \
	--v 1 --logtostderr \
	--go-header-file "${HEADER}" \
	--output-dir /tmp/openapi-gen-model-names \
	--output-pkg "${PACKAGE_NAME}/pkg/openapi" \
	--output-model-name-file zz_generated.model_name.go \
	"${PACKAGE_NAME}/pkg/apis/projectcalico/v3" \
	"${PACKAGE_NAME}/pkg/lib/numorstring"

# Generate openapi definitions. Must run before client-gen, which uses the
# output for --openapi-schema. Fail on any new API rule violations in Calico
# types (upstream k8s violations are ignored).
openapi-gen \
	--v 1 --logtostderr \
	--go-header-file "${HEADER}" \
	--output-dir "${REPO_ROOT}/pkg/openapi" \
	--output-pkg "${PACKAGE_NAME}/pkg/openapi" \
	"${PACKAGE_NAME}/pkg/apis/projectcalico/v3" \
	"k8s.io/api/core/v1" \
	"k8s.io/api/networking/v1" \
	"k8s.io/apimachinery/pkg/apis/meta/v1" \
	"k8s.io/apimachinery/pkg/runtime" \
	"k8s.io/apimachinery/pkg/util/intstr" \
	"k8s.io/apimachinery/pkg/version" \
	"${PACKAGE_NAME}/pkg/lib/numorstring" 2>&1 | tee /tmp/openapi-gen.log
grep "API rule violation" /tmp/openapi-gen.log | grep "projectcalico" > /tmp/openapi-violations.log || true
if [ -s /tmp/openapi-violations.log ]; then
	grep -v -F -f "${REPO_ROOT}/hack/openapi-violations-known.list" /tmp/openapi-violations.log > /tmp/openapi-new-violations.log || true
	if [ -s /tmp/openapi-new-violations.log ]; then
		echo "ERROR: openapi-gen reported new API rule violations in Calico types:"
		cat /tmp/openapi-new-violations.log
		echo "If these are intentional, add them to hack/openapi-violations-known.list"
		exit 1
	fi
fi

###############################################################################
# Client generation
###############################################################################
APPLY_CONFIG_PKG="${PACKAGE_NAME}/pkg/client/applyconfiguration_generated"
APPLY_CONFIG_DIR="${REPO_ROOT}/pkg/client/applyconfiguration_generated"

# Generate OpenAPI schema JSON for applyconfiguration-gen. This populates the
# structured-merge-diff type information in internal/internal.go, which is
# required for fake.NewClientset() to work correctly.
OPENAPI_SCHEMA=$(mktemp)
trap "rm -f ${OPENAPI_SCHEMA}" EXIT
echo "Generating OpenAPI schema for applyconfiguration-gen..."
go run "${REPO_ROOT}/hack/openapi-schema/" > "${OPENAPI_SCHEMA}"

# Generate apply configurations.
applyconfiguration-gen \
	--go-header-file "${HEADER}" \
	--openapi-schema "${OPENAPI_SCHEMA}" \
	--output-dir "${APPLY_CONFIG_DIR}" \
	--output-pkg "${APPLY_CONFIG_PKG}" \
	"${PACKAGE_NAME}/pkg/apis/projectcalico/v3"

# Patch applyconfiguration-gen bugs.
patch -p2 -d "${REPO_ROOT}" < "${REPO_ROOT}/patches/0002-Fix-duplicate-ensureProtoPort-method-in-FelixConfigurationSpec.patch"
patch -p2 -d "${REPO_ROOT}" < "${REPO_ROOT}/patches/0003-Fix-pointer-slice-append-in-IPAMBlockSpec-Allocations.patch"

# Generate the versioned clientset.
client-gen \
	--go-header-file "${HEADER}" \
	--input-base "${PACKAGE_NAME}/pkg/apis/" \
	--input "projectcalico/v3" \
	--output-dir "${REPO_ROOT}/pkg/client/clientset_generated" \
	--clientset-path "${PACKAGE_NAME}/pkg/client/clientset_generated/" \
	--clientset-name "clientset" \
	--apply-configuration-package "${APPLY_CONFIG_PKG}"

# Generate listers.
lister-gen \
	--go-header-file "${HEADER}" \
	--output-dir "${REPO_ROOT}/pkg/client/listers_generated" \
	--output-pkg "${PACKAGE_NAME}/pkg/client/listers_generated" \
	"${PACKAGE_NAME}/pkg/apis/projectcalico/v3"

# Generate informers.
informer-gen \
	--go-header-file "${HEADER}" \
	--versioned-clientset-package "${PACKAGE_NAME}/pkg/client/clientset_generated/clientset" \
	--listers-package "${PACKAGE_NAME}/pkg/client/listers_generated" \
	--output-dir "${REPO_ROOT}/pkg/client/informers_generated" \
	--output-pkg "${PACKAGE_NAME}/pkg/client/informers_generated" \
	"${PACKAGE_NAME}/pkg/apis/projectcalico/v3"
