//go:build ignore

// gen_compact.go generates compact_generated.go.
//
// Each compactN struct has a uint64 bitfield header followed by an
// [N]uniquestr.Handle array, giving the GC exact pointer information.
// allocCompact dispatches on slice length to allocate the right type.
//
// Usage: go run gen_compact.go

package main

import (
	"fmt"
	"os"
)

const maxKeys = 63

func main() {
	f, err := os.Create("compact_generated.go")
	if err != nil {
		panic(err)
	}
	defer f.Close()

	fmt.Fprint(f, `// Code generated by gen_compact.go; DO NOT EDIT.

package uniquelabels

import (
	"unsafe"

	"github.com/projectcalico/calico/lib/std/uniquestr"
)

`)

	// Struct types.
	for i := 1; i <= maxKeys; i++ {
		fmt.Fprintf(f, "type compact%d struct {\n", i)
		fmt.Fprint(f, "\tbitfield uint64\n")
		fmt.Fprintf(f, "\tvalues   [%d]uniquestr.Handle\n", i)
		fmt.Fprint(f, "}\n\n")
	}

	// allocCompact function.
	fmt.Fprint(f, `// allocCompact allocates an exactly-sized compact struct, sets its
// bitfield, and returns a *compactMap view.  The caller populates
// values via cm.slice().  Each struct type has a properly typed
// [N]Handle array so the GC traces all stored handles.
func allocCompact(bitfield uint64, n int) *compactMap {
	switch n {
	case 0:
		return (*compactMap)(unsafe.Pointer(&emptyBacking))
`)
	for i := 1; i <= maxKeys; i++ {
		fmt.Fprintf(f, "\tcase %d:\n", i)
		fmt.Fprintf(f, "\t\tp := new(compact%d)\n", i)
		fmt.Fprint(f, "\t\tp.bitfield = bitfield\n")
		fmt.Fprint(f, "\t\treturn (*compactMap)(unsafe.Pointer(p))\n")
	}
	fmt.Fprintf(f, `	default:
		panic("unreachable: compact map supports at most %d entries")
	}
}
`, maxKeys)
}
